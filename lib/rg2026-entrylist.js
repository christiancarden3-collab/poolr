// Get ranking by player name
export const getPlayerRanking = (name) => {
  const player = RG_ENTRY_LIST.find(p => p.n.toLowerCase().includes(name.toLowerCase()))
  return player?.r || null
}

// Get all players sorted by ranking
export const getPlayersByRanking = () => {
  return [...RG_ENTRY_LIST].sort((a, b) => a.r - b.r)
}
