// Generate group stage matches
export function generateGroupStageMatches() {
  const matches = []
  const groups = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L']
  
  let matchNumber = 1
  const startDate = new Date('2026-06-11')
  
  groups.forEach((group, groupIndex) => {
    const groupTeams = WC2026_TEAMS.filter(t => t.group === group)
    
    // Each team plays each other once (6 matches per group)
    // Match day 1: 1v4, 2v3
    // Match day 2: 1v3, 2v4  
    // Match day 3: 1v2, 3v4
    
    const matchups = [
      [0, 3], [1, 2], // Day 1
      [0, 2], [1, 3], // Day 2
      [0, 1], [2, 3], // Day 3
    ]
    
    matchups.forEach((matchup, matchIndex) => {
      const dayOffset = Math.floor(matchIndex / 2) * 4 + Math.floor(groupIndex / 4)
      const matchDate = new Date(startDate)
      matchDate.setDate(matchDate.getDate() + dayOffset)
      
      matches.push({
        id: `gs-${matchNumber}`,
        matchNumber,
        round: 'Group Stage',
        group,
        homeTeam: groupTeams[matchup[0]].code,
        awayTeam: groupTeams[matchup[1]].code,
        date: matchDate.toISOString().split('T')[0],
        time: ['14:00', '17:00', '20:00'][matchIndex % 3],
        venue: getVenue(groupIndex, matchIndex),
        status: 'scheduled',
        homeScore: null,
        awayScore: null,
      })
      matchNumber++
    })
  })
  
  return matches
}

// Generate knockout matches (placeholders)
export function generateKnockoutMatches() {
  const knockoutMatches = []
  let matchNumber = 73 // After 72 group stage matches
  
  // Round of 32 (16 matches)
  for (let i = 1; i <= 16; i++) {
    knockoutMatches.push({
      id: `r32-${i}`,
      matchNumber: matchNumber++,
      round: 'Round of 32',
      homeTeam: `Winner Group ${String.fromCharCode(64 + Math.ceil(i/2))}`,
      awayTeam: `Runner-up Group ${String.fromCharCode(64 + (i % 12) + 1)}`,
      date: '2026-07-01',
      status: 'scheduled',
    })
  }
  
  // Round of 16, Quarter, Semi, Final... (simplified)
  const rounds = [
    { name: 'Round of 16', count: 8, date: '2026-07-05' },
    { name: 'Quarter-finals', count: 4, date: '2026-07-09' },
    { name: 'Semi-finals', count: 2, date: '2026-07-15' },
    { name: 'Third Place', count: 1, date: '2026-07-18' },
    { name: 'Final', count: 1, date: '2026-07-19' },
  ]
  
  rounds.forEach(round => {
    for (let i = 1; i <= round.count; i++) {
      knockoutMatches.push({
        id: `${round.name.toLowerCase().replace(/[^a-z0-9]/g, '')}-${i}`,
        matchNumber: matchNumber++,
        round: round.name,
        homeTeam: 'TBD',
        awayTeam: 'TBD',
        date: round.date,
        status: 'scheduled',
      })
    }
  })
  
  return knockoutMatches
}

